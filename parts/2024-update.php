		<p>The entire reason I reopened this site's Git repo was to provide an update on what's been exciting me lately, and it's Game Dev. The past month I've been progressing through learning the basic of <a href="https://godotengine.org/">Godot</a>.</p>

		<p>I started with two very simple tutorials, but after completing the set, I realized how /bad/ tutorials can really be for development. You follow directions to a T and get X result. It's fast and addicting, and you feel like you're making fast progress. My worry with that is, if I were thrown into a project at any stage of development, and were asked to "do X, Y, or Z" I would have zero clue how to do anything. Because of that, I quickly ran far, far away from "tutorial hell" and instead opted to strategize a bit more for my hands-on type of learning style:</p>

		<p><b>I'm committing myself to learning this entire year</b>. I understand that getting into and even being successful at something like Game Dev is a lengthy journey. I'm set out to produce very small, extremely focused projects every 2-4 weeks based on a particular set of learning. This will be akin to spending 2 weeks on menus. 2 weeks on learning player movement, collisions, and hitboxes. 2 weeks on integrating art into animations, etc. This should allow me to be hyper focused on one thing at a time and learn as I go.</p>

		<p>Ultimately, if things go well, 2025 I want to try to publish a game myself.</p>

