		<p>To explain, the boss Cerberus periodically triggers a mechanic where it spawns three Summoned Souls, which I call its Ghost Phase. The ghost phase is costly to endure and can be very difficult to survive. There have been discoveries that show the Ghost Phase is triggered using a combination of Cerberus' HP threshold and player aggression.</p>

		<p>As its understood, to "avoid" the ghost phase reliably, the steps should be done as follows:</p>

		<p>
			1. On the first attack on Cerberus, start a timer for 0:56s.</br>
			2. Continue to attack Cerberus until its HP is just above 400. Taking HP below 400 will fail the No Ghosts method.</br>
			3. Wait for the timer to run out.</br>
			4. Once the timer is complete, continue fighting Cerberus.</br>
		</p>

		<p>Note: Once the 56 seconds are complete, it is best to use the highest DPS options available, as, I believe, another 0:5X second internal timer will start, and, once completed, if Cerberus is not yet defeated, another Ghost Phase will begin. This is why it's advisable to use Guthans during first attacks to ~ 400hp, then swap to highest DPS + spec weapons once the timer has completed.</p>
		<p>Note: If you attack Cerberus too early (with 1 second on the clock left) you can trigger an immediate Ghost Phase. It's important to wait until the "Time to Fight" alert has triggered before click Attack.</p>
		
